public class Money {

    private String value;

    public Money() {

    }

    public Money(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "Money{" + "value=" + value + '}';
    }

}
